COMMON_4DIGIT_PINS = {
    '0000', '1111', '1122', '1212', '1234', '4321', '5555',
    '1004', '2000', '6969', '8888', '1313', '1010', '2580'
}

COMMON_6DIGIT_PINS = {
    '000000', '111111', '112233', '123123', '123456', '654321',
    '121212', '456456', '789789', '101010', '111222', '999999'
}

def is_common_pin(pin):
    return pin in (COMMON_4DIGIT_PINS if len(pin) == 4 else COMMON_6DIGIT_PINS)